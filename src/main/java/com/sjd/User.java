package com.sjd;

public class User 
{
    private String firstname;

    private String surname;

    public String getFirstname ()
    {
        return firstname;
    }

    public void setFirstname (String name)
    {
        this.firstname = name;
    }

    public String getSurname ()
    {
        return surname;
    }

    public void setSurname (String name)
    {
        this.surname = name;
    }
}